    public void copyFile(String src,String dest){
        
    try{
            FileInputStream fin=new FileInputStream(src);
            FileOutputStream fout=new FileOutputStream(dest);
            
            int b=0;
            
            while((b = fin.read())!= -1){
               fout.write(b);
            }
               
            fout.close();
            fin.close();
            
        }catch(Exception ex){
            System.out.println(ex);
        }
    }   
  //    copy file to the system ends here

    
