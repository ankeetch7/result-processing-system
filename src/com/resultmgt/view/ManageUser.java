 // to load the roles to be assigned
     public void loadUserRoles(){
        UserRoleDAO urDAO = new UserRoleDAO();
                
        List<UserRoles> userRoleLists=urDAO.viewUserRoles();
           
            if(userRoleLists == null){
                jComboBox_Roles.addItem("Roles Not added");
                
            }else{
                for(int i=0;i<userRoleLists.size();i++){
                jComboBox_Roles.addItem(userRoleLists.get(i).getRole_name());
                
            }  
         }

    }
    

    // view user profile
    public void clearTableRows(){
        int rowCount=jTable_ViewUsers.getRowCount();
        for(int i=0;i<rowCount;i++){
            model.removeRow(0);
        }
    }
    
    public void loadData(){
        clearTableRows();
        UserDAO uDAO=new UserDAO();
        
        List<Users> userlists=uDAO.viewUsers();
        
        if(userlists == null){
            
        }else{
                for(int i=0;i<userlists.size();i++){
                model.addRow(new Object[]{
                userlists.get(i).getUser_id(),
                userlists.get(i).getFull_name(),
                userlists.get(i).getEmail(),
                userlists.get(i).getPhone() 
               });
            }
        }
    }   
    
