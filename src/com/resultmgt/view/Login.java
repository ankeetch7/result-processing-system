    private void jButton_loginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_loginActionPerformed
        
        // check if fields are empty
        if(jTextField_username.getText().trim().isEmpty() ||
            new String(jPasswordField_password.getPassword()).trim().isEmpty()){
            // it has empty fields
            JOptionPane.showMessageDialog(null, "Empty Fields");
        }else{
             String username = jTextField_username.getText().trim();
             String password =  new String(jPasswordField_password.getPassword()).trim();
             String role_name = jComboBox_Roles.getSelectedItem().toString();

            UserRoles urOb = new UserRoles();
            urOb.setRole_name(role_name);
            UserRoleDAO urDAO = new UserRoleDAO();
            int role_id = urDAO.getRoleId(urOb);
             
             Users uOb = new Users();
             uOb.setUsername(username);
             uOb.setPassword(password);
             uOb.setRole_name(role_name);
             uOb.setRole_id(role_id);
             
             UserDAO uDAO = new UserDAO();
             
             
             if(uDAO.loginUser(uOb) == 1){
                 TrackUser obj = new TrackUser();
                 obj.getRoles(uOb);
                 dispose();
             }
        }  
    }//GEN-LAST:event_jButton_loginActionPerformed
    
    

