    public void loadUserRoles(){
        UserRoleDAO urDAO = new UserRoleDAO();
                
        List<UserRoles> userRoleLists=urDAO.viewUserRoles();
           
            if(userRoleLists == null){
                jComboBox_Roles.addItem("Roles Not added");
                
            }else{
                for(int i=0;i<userRoleLists.size();i++){
                jComboBox_Roles.addItem(userRoleLists.get(i).getRole_name());
                }  
            }

    }

