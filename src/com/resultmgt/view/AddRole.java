    private void jButton_addRoleActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_addRoleActionPerformed
        
        if(jTextField_roleName.getText().trim().isEmpty() || 
                jTextArea_roleDesc.getText().trim().isEmpty()){
            JOptionPane.showMessageDialog(null, "Empty Fields");
        }
        else{
            String role_name = jTextField_roleName.getText().trim();
            String role_desc =  jTextArea_roleDesc.getText().trim();
            
            UserRoles urOb = new UserRoles();
            urOb.setRole_name(role_name);
            urOb.setRole_desc(role_desc);
            
            UserRoleDAO urDAO = new UserRoleDAO();
            if(urDAO.addRole(urOb) == 1){
                reset();
                JOptionPane.showMessageDialog(null, "Role added!");
            }else{
                JOptionPane.showMessageDialog(null, "Role not added!");
            }
            
        }
    }//GEN-LAST:event_jButton_addRoleActionPerformed

