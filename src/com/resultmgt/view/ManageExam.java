    private void jTable_ExamsMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable_ExamsMouseClicked
         int exam_id = (int)jTable_Exams.getValueAt(jTable_Exams.getSelectedRow(), 0);
         Exams eOb = new Exams();
         eOb.setExam_id(exam_id);
         ExamDAO eDAO = new ExamDAO();
         
         List<Exams> examList = eDAO.viewSingleExam(eOb);           
          try{
              
            jTextField_eExamId.setText(""+examList.get(0).getExam_id()); 
            jTextField_eExamName.setText(""+examList.get(0).getExam_name()); 
            
          
             SimpleDateFormat sdf = new SimpleDateFormat("dd-MM-yyyy");
              
               Date Startdate = sdf.parse((String)examList.get(0).getExam_start_date());

            jDateChooser_eExamStartDate.setDate(Startdate);
            Date EndDate = sdf.parse((String)examList.get(0).getExam_end_date());
           
            jDateChooser_eExamEndDate.setDate(EndDate);
            jTextArea_eExamDesc.setText(examList.get(0).getExam_desc());
              
          }catch(Exception ex){
              System.out.println(ex);
          }
         
    }//GEN-LAST:event_jTable_ExamsMouseClicked


       
