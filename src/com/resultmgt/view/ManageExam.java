    private void jButton_addActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_addActionPerformed
        
        String exam_name = jComboBox_ExamName.getSelectedItem().toString();
        SimpleDateFormat sdf = new SimpleDateFormat("dd-MM-yyyy");
        String exam_start_date = sdf.format(jDateChooser_ExamStartDate.getDate());
        
        String exam_end_date = sdf.format(jDateChooser_ExamEndDate.getDate());
        String exam_desc = jTextArea_desc.getText();
        
        if(exam_name.isEmpty() || exam_desc.isEmpty()){
            JOptionPane.showMessageDialog(null, "Fields are empty!");
        }else{
            
            String string_validation = "^[a-zA-Z ]+$";
            
            if(!exam_desc.matches(string_validation)){
                JOptionPane.showMessageDialog(null, "Description cannot be addded to name fields!");
            }else{
               Exams eOb = new Exams();
                eOb.setExam_name(exam_name);
                eOb.setExam_start_date(exam_start_date);
                eOb.setExam_end_date(exam_end_date);
                eOb.setExam_desc(exam_desc);
                
                ExamDAO eDAO = new ExamDAO();
                
                if(eDAO.insertData(eOb) == 1){
                    JOptionPane.showMessageDialog(null, "Exam added successfully!");
                    loadData();
                    reset();
                }else{
                    JOptionPane.showMessageDialog(null, "Exam added failed!");
                } 
            }       
            
        }
        
    }//GEN-LAST:event_jButton_addActionPerformed

